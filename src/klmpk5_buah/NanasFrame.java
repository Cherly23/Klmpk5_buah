    private void jTampilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTampilActionPerformed
        // TODO add your handling code here:
        Aziz_Nanas Nns = new Aziz_Nanas();
        
        if (tNamaBuah.getText().isEmpty()){
            Nns.setNama_buah("Tidak diketahui");
        } else {
            Nns.setNama_buah(tNamaBuah.getText());
        }
        if (tWarnaBuah.getText().isEmpty()){
            Nns.setWarna("Tidak diketahui");
        } else {
            Nns.setWarna(tWarnaBuah.getText());
        }
        if (tRasa.getText().isEmpty()){
            Nns.setRasa("Tidak diketahui");
        } else {
            Nns.setRasa(tNamaBuah.getText());
        }
        if (tBerat.getText().isEmpty()){
            Nns.setBerat(0);
        } else{
             Nns.setBerat(Double.parseDouble(tBerat.getText()));
        }
        if (tHarga.getText().isEmpty()){
            Nns.setHarga(0);
        }else {
            Nns.setHarga(Double.parseDouble(tHarga.getText()));
        }
        
        tNamaBuah2.setText(Nns.getNama_buah());
        tWarna2.setText(Nns.getWarna());
        tRasa1.setText(Nns.getRasa());
        tBerat1.setText(String.valueOf(Nns.getBerat()));
        tHarga1.setText(String.valueOf(Nns.getHarga()));
        
        if (jYa.isSelected()){
            tKulit2.setText("Ya, Bisa dimakan");
        }else{
            tKulit2.setText("Tidak, Kulit tidak Bisa dimakan");
        }
    }//GEN-LAST:event_jTampilActionPerformed

